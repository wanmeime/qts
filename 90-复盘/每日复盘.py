#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
QTS 90-复盘层 - 每日复盘主程序
功能：
  1. 整合盈亏追踪、策略归因、参数优化三大模块
  2. 生成每日完整复盘报告（Markdown）
  3. 支持命令行指定日期运行
  4. 自动关联回测与实盘数据

使用方法：
  python3 每日复盘.py              # 复盘今天
  python3 每日复盘.py 20260602     # 复盘指定日期
  python3 每日复盘.py --register   # 注册参数版本
"""

import json
import os
import sys
from datetime import datetime
from pathlib import Path

# 导入子模块
sys.path.insert(0, str(Path(__file__).parent))
from 盈亏追踪 import 计算当日盈亏, 生成盈亏Markdown
from 策略归因 import 计算策略归因, 生成归因Markdown
from 参数优化记录 import (
    从回测自动注册, 生成参数优化Markdown, 
    关联实盘指标, 加载参数历史
)


# ========== 路径配置 ==========
BASE_DIR = Path("/home/jiaod/qts")
持仓文件 = BASE_DIR / "40-执行/持仓/当前持仓.json"
订单目录 = BASE_DIR / "40-执行/订单"
风控报告文件 = BASE_DIR / "40-执行/风控/风控检查报告.md"
日报目录 = BASE_DIR / "90-复盘/日报"


def 读取文件(路径):
    """读取文本文件"""
    with open(路径, 'r', encoding='utf-8') as f:
        return f.read()


def 读取JSON(路径):
    """读取JSON文件"""
    with open(路径, 'r', encoding='utf-8') as f:
        return json.load(f)


def 生成复盘报告(日期字符串):
    """
    生成每日完整复盘报告
    整合所有子模块的分析结果
    """
    print(f"📋 开始生成 {日期字符串} 复盘报告...")
    print()
    
    # ========== 步骤1: 盈亏追踪 ==========
    print("  [1/4] 计算盈亏...")
    盈亏记录 = 计算当日盈亏(日期字符串)
    if not 盈亏记录:
        print("  ❌ 盈亏计算失败")
        return None
    print(f"  ✅ 总资产: ¥{盈亏记录['账户概览']['总资产']:,.2f}")
    
    # ========== 步骤2: 策略归因 ==========
    print("  [2/4] 策略归因分析...")
    归因结果 = 计算策略归因(日期字符串)
    if not 归因结果:
        print("  ❌ 归因分析失败")
        return None
    print(f"  ✅ 分析了 {len(归因结果['策略分析'])} 个策略")
    
    # ========== 步骤3: 参数优化 ==========
    print("  [3/4] 参数版本管理...")
    参数结果 = 从回测自动注册()
    # 关联实盘指标到当前参数版本
    for 策略名, 分析 in 归因结果["策略分析"].items():
        实盘指标 = {
            "总市值": 分析["总市值"],
            "总盈亏": 分析["总盈亏"],
            "收益率": 分析["收益率"],
            "持仓数": 分析["持仓数"],
            "平均信号强度": 分析["平均信号强度"]
        }
        关联实盘指标(策略名, 日期字符串, 实盘指标)
    print(f"  ✅ 已关联 {len(归因结果['策略分析'])} 个策略的实盘指标")
    
    # ========== 步骤4: 组装报告 ==========
    print("  [4/4] 生成报告...")
    
    概览 = 盈亏记录["账户概览"]
    报告 = []
    
    # --- 报告头 ---
    报告.append(f"# QTS每日复盘报告 - {日期字符串}")
    报告.append("")
    报告.append(f"> 生成时间：{datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    报告.append(f"> 初始资金：¥1,000,000.00 | 当前净值：{概览['净值']:.6f}")
    报告.append("")
    报告.append("---")
    报告.append("")
    
    # --- 第一部分：账户总览 ---
    报告.append("## 一、账户总览")
    报告.append("")
    报告.append("| 指标 | 数值 | 说明 |")
    报告.append("|------|------|------|")
    报告.append(f"| 总资产 | ¥{概览['总资产']:,.2f} | 持仓市值+可用资金 |")
    报告.append(f"| 总市值 | ¥{概览['总市值']:,.2f} | 持仓股票市值 |")
    报告.append(f"| 可用资金 | ¥{概览['可用资金']:,.2f} | 账户现金 |")
    报告.append(f"| 持仓比例 | {概览['持仓比例']:.2f}% | 市值/总资产 |")
    报告.append(f"| 持仓股数 | {概览['持仓股数']} 只 | 当前持股数量 |")
    报告.append(f"| 总浮动盈亏 | ¥{概览['总浮动盈亏']:+,.2f} | 持仓浮盈浮亏 |")
    报告.append(f"| 总手续费 | ¥{概览['总手续费']:,.2f} | 交易佣金成本 |")
    报告.append(f"| 当日净值 | {概览['净值']:.6f} | 总资产/初始资金 |")
    报告.append(f"| 当日收益率 | {概览['当日收益率']:+.4f}% | 与前一交易日比 |")
    报告.append(f"| 累计收益率 | {概览['累计收益率']:+.4f}% | 自建仓以来 |")
    报告.append("")
    报告.append("---")
    报告.append("")
    
    # --- 第二部分：盈亏明细 ---
    报告.append(生成盈亏Markdown(盈亏记录))
    报告.append("---")
    报告.append("")
    
    # --- 第三部分：策略归因 ---
    报告.append(生成归因Markdown(归因结果))
    报告.append("---")
    报告.append("")
    
    # --- 第四部分：风控状态 ---
    报告.append("## 四、风控状态")
    报告.append("")
    if 风控报告文件.exists():
        风控内容 = 读取文件(风控报告文件)
        # 提取风控总结部分
        if "风控总结" in 风控内容:
            总结开始 = 风控内容.index("风控总结")
            报告.append(风控内容[总结开始:])
        else:
            报告.append("> 风控报告已生成，请查看完整报告。")
    else:
        报告.append("> 未找到风控报告。")
    报告.append("")
    
    # --- 第五部分：订单执行状态 ---
    报告.append("## 五、订单执行状态")
    报告.append("")
    订单文件 = 订单目录 / f"{日期字符串}-orders.json"
    if 订单文件.exists():
        订单列表 = 读取JSON(订单文件)
        报告.append(f"当日共 **{len(订单列表)}** 笔订单：")
        报告.append("")
        报告.append("| 订单ID | 股票 | 方向 | 价格 | 数量 | 金额 | 来源 | 状态 |")
        报告.append("|--------|------|------|------|------|------|------|------|")
        for 订单 in 订单列表:
            oid = 订单["订单ID"][-8:]  # 只显示后8位
            报告.append(
                f"| {oid} | {订单['名称']} | {订单['方向']} "
                f"| {订单['下单价格']:.2f} | {订单['下单数量']} "
                f"| ¥{订单['下单金额']:,.2f} | {订单['信号来源']} | {订单['状态']} |"
            )
        报告.append("")
    else:
        报告.append("> 未找到当日订单记录。")
    报告.append("")
    报告.append("---")
    报告.append("")
    
    # --- 第六部分：参数优化记录 ---
    报告.append(生成参数优化Markdown())
    报告.append("---")
    报告.append("")
    
    # --- 第七部分：次日操作计划 ---
    报告.append("## 七、次日操作计划")
    报告.append("")
    报告.append("### 需关注事项")
    报告.append("")
    
    # 自动生成关注事项
    关注事项 = []
    for stock in 盈亏记录["盈亏明细"]:
        if stock["浮动盈亏率"] < -3:
            关注事项.append(f"- ⚠️ **{stock['名称']}** 浮亏 {stock['浮动盈亏率']:.2f}%，接近止损线(-5%)")
    
    if 概览["持仓比例"] > 75:
        关注事项.append(f"- ⚠️ 总仓位 {概览['持仓比例']:.1f}%，接近80%上限")
    
    if 概览["可用资金"] < 100000:
        关注事项.append(f"- 💰 可用资金 ¥{概览['可用资金']:,.0f}，偏低，注意资金管理")
    
    for 策略名, 评级 in 归因结果["策略评级"].items():
        if 评级["星级"] <= 2:
            关注事项.append(f"- 📊 **{策略名}** 评级偏低({'⭐'*评级['星级']})，{评级['建议']}")
    
    if 关注事项:
        报告.extend(关注事项)
    else:
        报告.append("- ✅ 暂无需要特别关注的事项")
    
    报告.append("")
    报告.append("### 参数优化方向")
    报告.append("")
    
    参数历史 = 加载参数历史()
    当前版本 = 参数历史.get("当前版本", {})
    for 策略名, 版本 in 当前版本.items():
        建议 = 归因结果["策略评级"].get(策略名, {}).get("建议", "保持当前参数")
        报告.append(f"- **{策略名}** ({版本['版本号']}): {建议}")
    
    报告.append("")
    报告.append("---")
    报告.append("")
    报告.append("*本报告由QTS量化交易系统90-复盘层自动生成*")
    
    # 保存报告
    报告内容 = "\n".join(报告)
    报告文件 = 日报目录 / f"{日期字符串}-复盘报告.md"
    os.makedirs(日报目录, exist_ok=True)
    with open(报告文件, 'w', encoding='utf-8') as f:
        f.write(报告内容)
    
    print()
    print(f"✅ 复盘报告已生成: {报告文件}")
    print(f"   报告大小: {len(报告内容):,} 字符, {len(报告)} 行")
    
    return 报告文件


if __name__ == "__main__":
    print("=" * 60)
    print("  QTS量化交易系统 - 90-复盘层")
    print("  每日盈亏追踪 + 策略归因 + 参数优化记录")
    print("=" * 60)
    print()
    
    # 参数处理
    if len(sys.argv) > 1:
        if sys.argv[1] == "--register":
            print("📝 注册参数版本...")
            结果 = 从回测自动注册()
            for 策略名, 版本 in 结果:
                print(f"  ✅ {策略名} -> {版本['版本号']}")
            sys.exit(0)
        elif sys.argv[1] == "--help":
            print("用法:")
            print("  python3 每日复盘.py              # 复盘今天")
            print("  python3 每日复盘.py 20260602     # 复盘指定日期")
            print("  python3 每日复盘.py --register   # 注册参数版本")
            sys.exit(0)
        else:
            日期 = sys.argv[1]
    else:
        日期 = datetime.now().strftime("%Y%m%d")
    
    报告文件 = 生成复盘报告(日期)
    if 报告文件:
        print(f"\n🎉 {日期} 复盘完成！")
