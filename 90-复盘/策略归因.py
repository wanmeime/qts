"""
QTS 90-复盘层 - 策略归因分析模块
功能：
  1. 按策略维度分析盈亏贡献
  2. 对比回测指标与实盘表现
  3. 分析行业/个股贡献度
  4. 生成策略归因报告
"""

import json
import os
import sys
from datetime import datetime
from pathlib import Path


# ========== 配置 ==========
BASE_DIR = Path("/home/jiaod/qts")
持仓文件 = BASE_DIR / "40-执行/持仓/当前持仓.json"
回测目录 = BASE_DIR / "20-回测"
盈亏记录目录 = BASE_DIR / "90-复盘/盈亏记录"
策略归因目录 = BASE_DIR / "90-复盘/策略归因"
配置文件 = BASE_DIR / "90-复盘/复盘配置.json"


def 读取JSON(路径):
    """读取JSON文件"""
    with open(路径, 'r', encoding='utf-8') as f:
        return json.load(f)


def 保存JSON(路径, 数据):
    """保存JSON文件"""
    os.makedirs(os.path.dirname(路径), exist_ok=True)
    with open(路径, 'w', encoding='utf-8') as f:
        json.dump(数据, f, ensure_ascii=False, indent=2)


def 加载回测指标(策略名, 版本="20260602-v1"):
    """加载指定策略的回测指标"""
    if 策略名 == "动量轮动":
        路径 = 回测目录 / f"动量轮动策略/{版本}/指标.json"
        if 路径.exists():
            return 读取JSON(路径)
    elif 策略名 == "多因子选股":
        路径 = 回测目录 / f"多因子选股策略/{版本}/回测指标.json"
        if 路径.exists():
            return 读取JSON(路径)
    return None


def 加载回测参数(策略名, 版本="20260602-v1"):
    """加载策略回测参数"""
    if 策略名 == "动量轮动":
        路径 = 回测目录 / f"动量轮动策略/{版本}/指标.json"
        if 路径.exists():
            data = 读取JSON(路径)
            return data.get("参数", {})
    elif 策略名 == "多因子选股":
        路径 = 回测目录 / f"多因子选股策略/{版本}/回测指标.json"
        if 路径.exists():
            return {}  # 多因子的参数在回测报告文本中
    return {}


def 计算策略归因(日期字符串):
    """
    执行策略归因分析
    返回归因分析结果字典
    """
    # 读取盈亏记录
    盈亏文件 = 盈亏记录目录 / f"{日期字符串}-盈亏记录.json"
    if not 盈亏文件.exists():
        print(f"⚠️ 未找到盈亏记录: {盈亏文件}")
        return None
    
    盈亏记录 = 读取JSON(盈亏文件)
    持仓数据 = 读取JSON(持仓文件)
    
    归因结果 = {
        "日期": 日期字符串,
        "分析时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        "策略分析": {},
        "行业分析": {},
        "个股贡献排名": [],
        "回测vs实盘": {},
        "策略评级": {}
    }
    
    持仓明细 = 持仓数据["持仓明细"]
    总资产 = 持仓数据["持仓汇总"]["总资产"]
    
    # ========== 1. 按策略维度分析 ==========
    策略分组 = {}
    for stock in 持仓明细:
        策略 = stock.get("信号来源", "未知")
        if 策略 not in 策略分组:
            策略分组[策略] = []
        策略分组[策略].append(stock)
    
    for 策略名, 股票列表 in 策略分组.items():
        总市值 = sum(s["市值"] for s in 股票列表)
        总盈亏 = sum((s["当前价"] - s["成本价"]) * s["持股数量"] for s in 股票列表)
        总手续费 = sum(s["手续费"] for s in 股票列表)
        平均信号强度 = sum(s.get("信号强度", 0) for s in 股票列表) / len(股票列表)
        
        # 计算该策略的收益率
        收益率 = 总盈亏 / 总市值 * 100 if 总市值 > 0 else 0
        仓位占比 = 总市值 / 总资产 * 100
        
        # 加载回测指标对比
        回测指标 = 加载回测指标(策略名)
        回测参数 = 加载回测参数(策略名)
        
        归因结果["策略分析"][策略名] = {
            "持仓数": len(股票列表),
            "总市值": round(总市值, 2),
            "总盈亏": round(总盈亏, 2),
            "总手续费": round(总手续费, 2),
            "收益率": round(收益率, 4),
            "仓位占比": round(仓位占比, 2),
            "平均信号强度": round(平均信号强度, 2),
            "重仓股": [
                {"名称": s["名称"], "市值": s["市值"], "仓位": round(s["市值"]/总资产*100, 2)}
                for s in sorted(股票列表, key=lambda x: x["市值"], reverse=True)[:3]
            ],
            "回测指标": {},
            "回测参数": 回测参数
        }
        
        # 回测指标对比
        if 回测指标:
            if 策略名 == "动量轮动":
                绩效 = 回测指标.get("绩效指标", {})
                归因结果["策略分析"][策略名]["回测指标"] = {
                    "年化收益率": 绩效.get("年化收益率", "N/A"),
                    "夏普比率": 绩效.get("夏普比率", "N/A"),
                    "最大回撤": 绩效.get("最大回撤", "N/A"),
                    "胜率": 绩效.get("胜率", "N/A"),
                    "总交易次数": 绩效.get("总交易次数", "N/A")
                }
            elif 策略名 == "多因子选股":
                归因结果["策略分析"][策略名]["回测指标"] = {
                    "年化收益率": f"{回测指标.get('annual_return', 0)*100:.2f}%",
                    "夏普比率": round(回测指标.get("sharpe_ratio", 0), 4),
                    "最大回撤": f"{回测指标.get('max_drawdown', 0)*100:.2f}%",
                    "Calmar比率": round(回测指标.get("calmar_ratio", 0), 4),
                    "年化波动率": f"{回测指标.get('volatility', 0)*100:.2f}%"
                }
    
    # ========== 2. 个股贡献排名 ==========
    个股贡献 = []
    for stock in 持仓明细:
        浮动盈亏 = (stock["当前价"] - stock["成本价"]) * stock["持股数量"]
        对总收益贡献 = 浮动盈亏 / 总资产 * 100
        个股贡献.append({
            "代码": stock["代码"],
            "名称": stock["名称"],
            "浮动盈亏": round(浮动盈亏, 2),
            "盈亏率": round((stock["当前价"] - stock["成本价"]) / stock["成本价"] * 100, 4),
            "对总收益贡献": round(对总收益贡献, 4),
            "信号来源": stock.get("信号来源", "未知")
        })
    
    个股贡献.sort(key=lambda x: x["浮动盈亏"], reverse=True)
    归因结果["个股贡献排名"] = 个股贡献
    
    # ========== 3. 策略评级 ==========
    for 策略名, 分析 in 归因结果["策略分析"].items():
        评级分数 = 0
        评级说明 = []
        
        # 基于信号强度评分
        if 分析["平均信号强度"] >= 85:
            评级分数 += 3
            评级说明.append("信号强度优秀(≥85)")
        elif 分析["平均信号强度"] >= 70:
            评级分数 += 2
            评级说明.append("信号强度良好(≥70)")
        else:
            评级分数 += 1
            评级说明.append("信号强度一般(<70)")
        
        # 基于回测指标评分
        回测 = 分析["回测指标"]
        if 回测:
            夏普 = 回测.get("夏普比率", 0)
            if isinstance(夏普, (int, float)):
                if 夏普 >= 2:
                    评级分数 += 3
                    评级说明.append("夏普比率优秀(≥2)")
                elif 夏普 >= 1:
                    评级分数 += 2
                    评级说明.append("夏普比率良好(≥1)")
                else:
                    评级分数 += 1
                    评级说明.append("夏普比率偏低(<1)")
        
        # 基于仓位合理性评分
        if 5 <= 分析["仓位占比"] <= 60:
            评级分数 += 2
            评级说明.append("仓位配置合理")
        else:
            评级分数 += 1
            评级说明.append("仓位配置需关注")
        
        总分 = 评级分数
        星级 = min(5, max(1, round(总分 / 2)))
        
        归因结果["策略评级"][策略名] = {
            "总分": 总分,
            "星级": 星级,
            "评语": "、".join(评级说明),
            "建议": 生成优化建议(策略名, 分析)
        }
    
    # 保存归因结果
    归因文件 = 策略归因目录 / f"{日期字符串}-策略归因.json"
    保存JSON(归因文件, 归因结果)
    
    return 归因结果


def 生成优化建议(策略名, 分析):
    """根据分析结果生成优化建议"""
    建议列表 = []
    
    if 分析["平均信号强度"] < 75:
        建议列表.append("建议提高信号强度阈值，过滤弱信号")
    
    回测 = 分析["回测指标"]
    if 回测:
        最大回撤 = 回测.get("最大回撤", "0%")
        if isinstance(最大回撤, str) and "%" in 最大回撤:
            回撤值 = float(最大回撤.replace("%", ""))
            if 回撤值 > 20:
                建议列表.append(f"最大回撤({最大回撤})偏大，建议增加止损条件")
        
        胜率 = 回测.get("胜率", "N/A")
        if isinstance(胜率, str) and "%" in 胜率:
            胜率值 = float(胜率.replace("%", ""))
            if 胜率值 < 50:
                建议列表.append(f"胜率({胜率})偏低，建议优化入场条件")
    
    if 分析["仓位占比"] > 50:
        建议列表.append(f"该策略仓位占比({分析['仓位占比']:.1f}%)较高，注意分散风险")
    
    if not 建议列表:
        建议列表.append("当前参数表现良好，可继续保持")
    
    return "；".join(建议列表)


def 生成归因Markdown(归因结果):
    """生成策略归因的Markdown片段"""
    lines = []
    lines.append("## 三、策略归因分析")
    lines.append("")
    
    # 策略对比
    lines.append("### 策略维度分析")
    lines.append("")
    lines.append("| 策略 | 持仓数 | 总市值 | 浮动盈亏 | 收益率 | 仓位占比 | 平均信号强度 | 评级 |")
    lines.append("|------|--------|--------|---------|--------|---------|-------------|------|")
    
    for 策略名, 分析 in 归因结果["策略分析"].items():
        盈亏符号 = "+" if 分析["总盈亏"] >= 0 else ""
        收益率符号 = "+" if 分析["收益率"] >= 0 else ""
        评级 = 归因结果["策略评级"].get(策略名, {})
        星级 = "⭐" * 评级.get("星级", 3)
        lines.append(
            f"| {策略名} | {分析['持仓数']} | ¥{分析['总市值']:,.2f} "
            f"| {盈亏符号}¥{分析['总盈亏']:,.2f} | {收益率符号}{分析['收益率']:.4f}% "
            f"| {分析['仓位占比']:.2f}% | {分析['平均信号强度']:.1f} | {星级} |"
        )
    lines.append("")
    
    # 回测vs实盘对比
    lines.append("### 回测指标回顾")
    lines.append("")
    for 策略名, 分析 in 归因结果["策略分析"].items():
        if 分析["回测指标"]:
            lines.append(f"**{策略名}**:")
            lines.append("")
            lines.append("| 指标 | 回测值 |")
            lines.append("|------|--------|")
            for k, v in 分析["回测指标"].items():
                lines.append(f"| {k} | {v} |")
            lines.append("")
    
    # 个股贡献排名
    lines.append("### 个股贡献排名（Top 5）")
    lines.append("")
    lines.append("| 排名 | 代码 | 名称 | 浮动盈亏 | 盈亏率 | 对总收益贡献 | 策略来源 |")
    lines.append("|------|------|------|---------|--------|-------------|---------|")
    
    for i, stock in enumerate(归因结果["个股贡献排名"][:5], 1):
        盈亏符号 = "+" if stock["浮动盈亏"] >= 0 else ""
        贡献符号 = "+" if stock["对总收益贡献"] >= 0 else ""
        lines.append(
            f"| {i} | {stock['代码']} | {stock['名称']} "
            f"| {盈亏符号}¥{stock['浮动盈亏']:,.2f} | {盈亏符号}{stock['盈亏率']:.4f}% "
            f"| {贡献符号}{stock['对总收益贡献']:.4f}% | {stock['信号来源']} |"
        )
    lines.append("")
    
    # 策略优化建议
    lines.append("### 策略优化建议")
    lines.append("")
    for 策略名, 评级 in 归因结果["策略评级"].items():
        lines.append(f"- **{策略名}** ({'⭐'*评级['星级']}): {评级['建议']}")
    lines.append("")
    
    return "\n".join(lines)


