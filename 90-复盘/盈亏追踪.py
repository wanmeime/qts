#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
QTS 90-复盘层 - 每日盈亏追踪模块
功能：
  1. 读取当日持仓快照，计算每只股票盈亏
  2. 与前一日持仓对比，识别持仓变动
  3. 累计追踪账户净值曲线
  4. 输出盈亏记录到JSON和Markdown
"""

import json
import os
import sys
from datetime import datetime, timedelta
from pathlib import Path


# ========== 配置 ==========
BASE_DIR = Path("/home/jiaod/qts")
持仓文件 = BASE_DIR / "40-执行/持仓/当前持仓.json"
历史持仓目录 = BASE_DIR / "40-执行/持仓/历史持仓"
盈亏记录目录 = BASE_DIR / "90-复盘/盈亏记录"
日报目录 = BASE_DIR / "90-复盘/日报"


def 读取JSON(路径):
    """读取JSON文件并返回字典"""
    with open(路径, 'r', encoding='utf-8') as f:
        return json.load(f)


def 保存JSON(路径, 数据):
    """保存字典到JSON文件"""
    os.makedirs(os.path.dirname(路径), exist_ok=True)
    with open(路径, 'w', encoding='utf-8') as f:
        json.dump(数据, f, ensure_ascii=False, indent=2)


def 加载历史净值():
    """加载历史净值记录"""
    净值文件 = 盈亏记录目录 / "净值曲线.json"
    if 净值文件.exists():
        return 读取JSON(净值文件)
    return []


def 保存历史净值(记录列表):
    """保存历史净值记录"""
    净值文件 = 盈亏记录目录 / "净值曲线.json"
    保存JSON(净值文件, 记录列表)


def 计算当日盈亏(日期字符串):
    """
    计算当日盈亏明细
    返回：盈亏汇总字典
    """
    # 读取当日持仓
    持仓数据 = 读取JSON(持仓文件)
    持仓汇总 = 持仓数据["持仓汇总"]
    持仓明细 = 持仓数据["持仓明细"]
    
    # 读取前一日持仓（如果存在）
    前一日持仓 = {}
    历史文件 = 历史持仓目录 / f"{获取前一交易日(日期字符串)}.json"
    if 历史文件.exists():
        前一日数据 = 读取JSON(历史文件)
        for stock in 前一日数据.get("持仓明细", []):
            前一日持仓[stock["代码"]] = stock
    
    # 逐只计算盈亏
    盈亏明细 = []
    策略盈亏汇总 = {}  # 按策略归因
    
    for stock in 持仓明细:
        代码 = stock["代码"]
        名称 = stock["名称"]
        成本价 = stock["成本价"]
        当前价 = stock["当前价"]
        持股数量 = stock["持股数量"]
        市值 = stock["市值"]
        手续费 = stock["手续费"]
        信号来源 = stock.get("信号来源", "未知")
        信号强度 = stock.get("信号强度", 0)
        
        # 计算浮动盈亏
        浮动盈亏 = (当前价 - 成本价) * 持股数量
        浮动盈亏率 = (当前价 - 成本价) / 成本价 * 100 if 成本价 > 0 else 0
        
        # 判断持仓变动
        持仓状态 = "持有"
        if 代码 not in 前一日持仓:
            持仓状态 = "新买入"
        elif 前一日持仓[代码]["持股数量"] != 持股数量:
            持仓状态 = "加仓" if 持股数量 > 前一日持仓[代码]["持股数量"] else "减仓"
        
        明细 = {
            "代码": 代码,
            "名称": 名称,
            "成本价": round(成本价, 4),
            "当前价": round(当前价, 2),
            "持股数量": 持股数量,
            "市值": round(市值, 2),
            "浮动盈亏": round(浮动盈亏, 2),
            "浮动盈亏率": round(浮动盈亏率, 4),
            "手续费": round(手续费, 2),
            "信号来源": 信号来源,
            "信号强度": 信号强度,
            "持仓状态": 持仓状态
        }
        盈亏明细.append(明细)
        
        # 策略归因汇总
        if 信号来源 not in 策略盈亏汇总:
            策略盈亏汇总[信号来源] = {
                "持仓数": 0, "总市值": 0, "总盈亏": 0, "股票列表": []
            }
        策略盈亏汇总[信号来源]["持仓数"] += 1
        策略盈亏汇总[信号来源]["总市值"] += 市值
        策略盈亏汇总[信号来源]["总盈亏"] += 浮动盈亏
        策略盈亏汇总[信号来源]["股票列表"].append(名称)
    
    # 计算账户整体
    总浮动盈亏 = sum(item["浮动盈亏"] for item in 盈亏明细)
    总手续费 = sum(item["手续费"] for item in 盈亏明细)
    总资产 = 持仓汇总["总资产"]
    总市值 = 持仓汇总["总市值"]
    可用资金 = 持仓汇总["可用资金"]
    初始资金 = 持仓汇总["初始资金"]
    
    # 加载历史净值计算累计收益
    历史净值 = 加载历史净值()
    前日净值 = 历史净值[-1]["净值"] if 历史净值 else 1.0
    当日净值 = 总资产 / 初始资金
    当日收益率 = (当日净值 - 前日净值) / 前日净值 * 100
    累计收益率 = (当日净值 - 1) * 100
    
    # 构建当日盈亏记录
    当日记录 = {
        "日期": 日期字符串,
        "记录时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        "账户概览": {
            "总资产": round(总资产, 2),
            "总市值": round(总市值, 2),
            "可用资金": round(可用资金, 2),
            "持仓比例": round(总市值 / 总资产 * 100, 2),
            "持仓股数": len(持仓明细),
            "总浮动盈亏": round(总浮动盈亏, 2),
            "总手续费": round(总手续费, 2),
            "净值": round(当日净值, 6),
            "当日收益率": round(当日收益率, 4),
            "累计收益率": round(累计收益率, 4)
        },
        "盈亏明细": 盈亏明细,
        "策略归因汇总": {}
    }
    
    # 策略归因汇总（含收益率）
    for 策略名, 汇总 in 策略盈亏汇总.items():
        收益率 = 汇总["总盈亏"] / 汇总["总市值"] * 100 if 汇总["总市值"] > 0 else 0
        当日记录["策略归因汇总"][策略名] = {
            "持仓数": 汇总["持仓数"],
            "总市值": round(汇总["总市值"], 2),
            "总盈亏": round(汇总["总盈亏"], 2),
            "收益率": round(收益率, 4),
            "占总仓位比": round(汇总["总市值"] / 总市值 * 100, 2),
            "股票列表": 汇总["股票列表"]
        }
    
    # 更新净值曲线
    净值点 = {
        "日期": 日期字符串,
        "净值": round(当日净值, 6),
        "总资产": round(总资产, 2),
        "当日收益率": round(当日收益率, 4),
        "累计收益率": round(累计收益率, 4),
        "持仓股数": len(持仓明细)
    }
    
    # 避免重复添加同一天
    历史净值 = [h for h in 历史净值 if h["日期"] != 日期字符串]
    历史净值.append(净值点)
    历史净值.sort(key=lambda x: x["日期"])
    保存历史净值(历史净值)
    
    # 保存当日盈亏记录
    记录文件 = 盈亏记录目录 / f"{日期字符串}-盈亏记录.json"
    保存JSON(记录文件, 当日记录)
    
    return 当日记录


def 获取前一交易日(日期字符串):
    """简单获取前一日期（实际应使用交易日历）"""
    dt = datetime.strptime(日期字符串, "%Y%m%d")
    前一天 = dt - timedelta(days=1)
    # 跳过周末
    while 前一天.weekday() >= 5:
        前一天 -= timedelta(days=1)
    return 前一天.strftime("%Y%m%d")


def 生成盈亏Markdown(当日记录):
    """生成盈亏追踪的Markdown片段"""
    概览 = 当日记录["账户概览"]
    明细 = 当日记录["盈亏明细"]
    策略汇总 = 当日记录["策略归因汇总"]
    
    lines = []
    lines.append("## 二、当日盈亏明细")
    lines.append("")
    
    # 账户概览
    lines.append("### 账户概览")
    lines.append("")
    lines.append("| 指标 | 数值 |")
    lines.append("|------|------|")
    lines.append(f"| 总资产 | ¥{概览['总资产']:,.2f} |")
    lines.append(f"| 总市值 | ¥{概览['总市值']:,.2f} |")
    lines.append(f"| 可用资金 | ¥{概览['可用资金']:,.2f} |")
    lines.append(f"| 持仓比例 | {概览['持仓比例']:.2f}% |")
    lines.append(f"| 持仓股数 | {概览['持仓股数']} 只 |")
    lines.append(f"| 总浮动盈亏 | ¥{概览['总浮动盈亏']:,.2f} |")
    lines.append(f"| 总手续费 | ¥{概览['总手续费']:,.2f} |")
    lines.append(f"| 当日净值 | {概览['净值']:.6f} |")
    
    当日收益率_str = f"+{概览['当日收益率']:.4f}%" if 概览['当日收益率'] >= 0 else f"{概览['当日收益率']:.4f}%"
    累计收益率_str = f"+{概览['累计收益率']:.4f}%" if 概览['累计收益率'] >= 0 else f"{概览['累计收益率']:.4f}%"
    lines.append(f"| 当日收益率 | {当日收益率_str} |")
    lines.append(f"| 累计收益率 | {累计收益率_str} |")
    lines.append("")
    
    # 个股盈亏
    lines.append("### 个股盈亏明细")
    lines.append("")
    lines.append("| 代码 | 名称 | 成本价 | 当前价 | 数量 | 市值 | 浮动盈亏 | 盈亏率 | 状态 | 信号来源 |")
    lines.append("|------|------|--------|--------|------|------|---------|--------|------|---------|")
    
    for s in sorted(明细, key=lambda x: x["浮动盈亏"], reverse=True):
        盈亏符号 = "+" if s["浮动盈亏"] >= 0 else ""
        盈亏率符号 = "+" if s["浮动盈亏率"] >= 0 else ""
        状态emoji = {"新买入": "🆕", "加仓": "⬆️", "减仓": "⬇️", "持有": "➡️"}.get(s["持仓状态"], "➡️")
        lines.append(
            f"| {s['代码']} | {s['名称']} | {s['成本价']:.4f} | {s['当前价']:.2f} "
            f"| {s['持股数量']} | ¥{s['市值']:,.2f} "
            f"| {盈亏符号}¥{s['浮动盈亏']:,.2f} | {盈亏率符号}{s['浮动盈亏率']:.4f}% "
            f"| {状态emoji}{s['持仓状态']} | {s['信号来源']} |"
        )
    lines.append("")
    
    # 策略归因概览
    lines.append("### 策略盈亏归因")
    lines.append("")
    lines.append("| 策略 | 持仓数 | 总市值 | 总盈亏 | 收益率 | 仓位占比 | 重仓股 |")
    lines.append("|------|--------|--------|--------|--------|---------|--------|")
    
    for 策略名, 汇总 in sorted(策略汇总.items(), key=lambda x: x[1]["总盈亏"], reverse=True):
        盈亏符号 = "+" if 汇总["总盈亏"] >= 0 else ""
        收益率符号 = "+" if 汇总["收益率"] >= 0 else ""
        股票列表_str = "、".join(汇总["股票列表"][:3])
        if len(汇总["股票列表"]) > 3:
            股票列表_str += f"等{len(汇总['股票列表'])}只"
        lines.append(
            f"| {策略名} | {汇总['持仓数']} | ¥{汇总['总市值']:,.2f} "
            f"| {盈亏符号}¥{汇总['总盈亏']:,.2f} | {收益率符号}{汇总['收益率']:.4f}% "
            f"| {汇总['占总仓位比']:.2f}% | {股票列表_str} |"
        )
    lines.append("")
    
    return "\n".join(lines)


